export class User {
    constructor(
        name,
        surname,
        email,
        address,
        fiscalCode,
        faculty,
        cellphone,
    ) {
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.address = address;
        this.fiscalCode = fiscalCode;
        this.faculty = faculty;
        this.cellphone = cellphone;
    }
}